package com.company;
public class Data_Type_02
 {
     public static void main(String[] args)
     {
         int value_1 = 1124560, value_2 = 124520;                // Data Type in Java : int, char, float, double, long, byte, short, bool,  string
         byte value3 = 10, value4 = 40;                 // A constant value which can be assigned to the variable is called as literals
         short value5 = 550, value6 = 5460;
         char value7 = 'C', value8 = 'D';
         double value9 = 20.55623237d, value10 = 10.2323202d;
         float value11 = 2.44f;
         long value12 = 99999999999L;
         boolean value13 = true;
         String str = "Hello world";

         System.out.println("int Value 1 is "+value_1);
         System.out.println("int Value 2 is "+value_2);
         System.out.println("byte Value 3 is "+value3);
         System.out.println("byte Value 4 is "+value4);
         System.out.println("short Value 5 is "+value5);
         System.out.println("short Value 6 is "+value6);
         System.out.println("char Value 7 is "+value7);
         System.out.println("char Value 8 is "+value8);
         System.out.println("double Value 9 is "+value9);
         System.out.println("double Value 10 is "+value10);
         System.out.println("float Value 11 is "+value11);
         System.out.println("long Value 12 is "+value12);
         System.out.println("bool Value 13 is "+value13);
         System.out.println("String Value 14 is "+str);
     }
 }